/**
 * Resolves provider transformers and runs metadata extraction.
 *
 * @remarks
 * The transformer registry lets coordinators ask for "the transformer for this
 * provider" without importing provider modules directly. This keeps startup
 * lighter and preserves a single place for provider-to-class mapping.
 */
export class TransformerRegistry {
	private static readonly cache = new Map<Provider, TransformerCtor>();

	constructor(
		private readonly httpClient: HttpClient,
		private readonly progressManager: ProgressManager
	) {}

	private async resolveTransformer(provider: Provider): Promise<TransformerCtor> {
		const cached = TransformerRegistry.cache.get(provider);

		if (cached) return cached;

		const factory = transformerFactories[provider] ?? transformerFactories[Provider.Default];

		const TransformerClass = await factory();

		TransformerRegistry.cache.set(provider, TransformerClass);

		return TransformerClass;
	}

	/**
	 * Runs the matching transformer for a URL and request.
	 *
	 * @param url Target URL to transform.
	 * @param request Execution request containing the provider.
	 * @returns Provider metadata in the requested result shape.
	 */
	public async transform<TArgs extends ExecutionArgs, TResult>(url: string, request: TArgs): Promise<TResult> {
		const provider = request.provider ?? Provider.Default;

		const TransformerClass = await this.resolveTransformer(provider);

		const transformer = new TransformerClass(this.httpClient, this.progressManager);

		return (await transformer.transform(url, request)) as TResult;
	}
}
